def test_extract_raises_when_no_scpc_page_present() -> None:
    """Documents matched by the dispatcher but missing the SCPC data row
    (e.g. a TOC-only mention) must not be silently parsed into garbage."""
    extractor = GaapScpcExtractor()
    with pytest.raises(ValueError, match="could not locate the SCPC page"):
        extractor.extract(
            source=Path("/dev/null"),
            text="Statement of Changes in Partners' Capital is mentioned here only.",
            per_page_text=[
                "Cover page — index — references Statement of Changes in Partners' Capital",
                "Other unrelated page",
            ],
        )


def test_missing_required_field_error_lists_what_was_found() -> None:
    """Error messages must surface enough context to debug a parsing failure.

    A page that has the SCPC heading + 'beginning of year' anchor but lacks
    a parseable end-of-year row should fail loudly AND tell the operator
    which categories DID match, so the regex patterns can be extended.
    """
    extractor = GaapScpcExtractor()
    page = (
        "Synthetic Fund, L.P.\n"
        "Statement of Changes in Partners' Capital\n"
        "Year ended December 31, 2024\n"
        "Partners' capital, beginning of year $100,000 $1,000,000 $1,100,000\n"
        "Capital contributions $5,000 $50,000 $55,000\n"
        # No "Partners' capital, end of year" row here.
    )
    with pytest.raises(ValueError, match=r"missing required field\(s\).*nav_ending") as exc:
        extractor.extract(source=Path("/dev/null"), text=page, per_page_text=[page])
    msg = str(exc.value)
    # The message should also reference the categories that DID match,
    # so an operator can see what's already wired and what's missing.
    assert "nav_beginning" in msg
    assert "contributions_period" in msg


def test_confidence_lower_for_pro_rata_allocation_path(synthetic_scpc_pdf: Path) -> None:
    """KPMG-style broken-out P&L rows produce higher confidence than the
    CohnReznick-style 'Pro rata allocation' compact path."""
    statement = parse_statement(synthetic_scpc_pdf, use_llm_fallback=False)
    # Synthetic fixture uses the broken-out KPMG path, so high-confidence.
    assert statement.source_metadata.parse_confidence == 0.85


