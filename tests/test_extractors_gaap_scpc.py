def test_synthetic_scpc_skips_lp_only_invariants(synthetic_scpc_pdf: Path) -> None:
    """Commitment-identity / TVPI / DPI rely on LP-level fields the SCPC doesn't
    carry — verify() must skip them, not fail them."""
    statement = parse_statement(synthetic_scpc_pdf, use_llm_fallback=False)
    report = verify(statement)
    invariant_names = {r.name for r in report.results}
    assert "nav_roll_forward" in invariant_names
    assert "commitment_identity" not in invariant_names
    assert "tvpi_consistency" not in invariant_names
    assert "dpi_consistency" not in invariant_names


