class TestNormalizeText:
    def test_dollar_space_digit_collapses(self) -> None:
        assert "$4,900,000" in _normalize_text("$ 4 ,900,000")

    def test_kerning_split_on_first_comma_collapses(self) -> None:
        # CohnReznick: "9 18,000" → "918,000"
        assert "918,000" in _normalize_text("Capital contributions 2,000 9 18,000 9 20,000")

    def test_stray_leading_digit_collapses(self) -> None:
        # CohnReznick: stray "1" before "2,132,000"
        out = _normalize_text("Pro rata allocation 2,000 1 2,132,000 1 2,134,000")
        assert "12,132,000" in out
        assert "12,134,000" in out

    def test_kpmg_smashed_money_tokens_split(self) -> None:
        # KPMG: tightly-kerned "250,00024,750,00025,000,000" → 3 separate values
        out = _normalize_text("Capital contributions 250,00024,750,00025,000,000")
        assert "250,000 24,750,000 25,000,000" in out

    def test_does_not_split_inside_a_valid_number(self) -> None:
        # Must NOT corrupt "24,750,000" into "24,750 000" or similar
        out = _normalize_text("foo 24,750,000 bar")
        assert "24,750,000" in out
        assert "24,750 000" not in out


class TestParseMoney:
    def test_simple_value(self) -> None:
        assert _parse_money("$84,219,000") == Decimal("84219000")

    def test_parens_negative(self) -> None:
        assert _parse_money("(36,888,000)") == Decimal("-36888000")

    @pytest.mark.parametrize("dash", ["—", "–", "-"])  # noqa: RUF001  (em/en/hyphen variants in real PDFs)
    def test_dash_zero_placeholder(self, dash: str) -> None:
        assert _parse_money(dash) == Decimal("0")


class TestParsePeriod:
    def test_placeholder_year_substituted_with_2099(self) -> None:
        start, end, as_of = _parse_period("Year ended December 31, 20XX")
        assert end.year == 2099
        assert end.month == 12
        assert end.day == 31
        assert start == end.replace(year=2098, month=1, day=1)
        assert as_of == end

    def test_real_year(self) -> None:
        _, end, _ = _parse_period("Year Ended December 31, 2024")
        assert end.year == 2024


